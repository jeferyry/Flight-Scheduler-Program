    private void customer_status_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_customer_status_buttonActionPerformed
        // TODO add your handling code here:
        Object customer_combo_box = Customer_status_combo_box.getSelectedItem();
        
        ArrayList <String> flights = BookingEntry.getCustomerFlightStatus(customer_combo_box.toString());
        ArrayList <String> dates = BookingEntry.getCustomerDateStatus(customer_combo_box.toString());
        ArrayList <String> waitlist_flights = Waitlist.getWaitlistFlight(customer_combo_box.toString());
        ArrayList <String> waitlist_days = Waitlist.getWaitlistDay(customer_combo_box.toString());
        
        String status = "Flights: \n \n";
        
        for (int i = 0; i < flights.size(); i++) {
            String flight = flights.get(i);
            String date = dates.get(i);
            status = status + "Booked on " + flight + ", " + date + "\n";
            
        }
        
        for (int y = 0; y < waitlist_flights.size(); y++) {
            String waitlist_flight = waitlist_flights.get(y);
            String waitlist_day = waitlist_days.get(y);
            status = status + "Waitlisted on " + waitlist_flight + ", " + waitlist_day + "\n";
            
        }
        FlightStatusTextArea.setText(status);
    }//GEN-LAST:event_customer_status_buttonActionPerformed

    private void Drop_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Drop_buttonActionPerformed
        // TODO add your handling code here:
        Object dropped_flights = Delete_flight_combo_box.getSelectedItem();
        
        List customer_bookings = null;
        List days_bookings = null;
        List customer_waitlist = null;
        List days_waitlist = null;
        List AllFlights = null;
        
        new Flight().removeFlightsANDSeats(dropped_flights); //removes the flight from the database
        
        
        AllFlights = Flight.getAllFlights(); //arraylist
        
        customer_bookings = BookingEntry.getCustomerANDDayWithFlight(dropped_flights).get(0); //getting customer from the dropped flights
        days_bookings = BookingEntry.getCustomerANDDayWithFlight(dropped_flights).get(1); //getting days from the dropped flights
        
        customer_waitlist = Waitlist.getCustomerANDDayWithFlight(dropped_flights).get(0);
        days_waitlist = Waitlist.getCustomerANDDayWithFlight(dropped_flights).get(1);
        
        
        int i = 0;
        
        outerloop:
        for (int y = 0; y < customer_bookings.size(); y++) {
            i = 0;
            for (int z = 0; z < AllFlights.size(); z++) {
                if (BookingEntry.getFlightSeats(AllFlights.get(z).toString(), Date.valueOf(days_bookings.get(y).toString())) < Flight.getSeats((String) AllFlights.get(z))) {
                    new BookingEntry().removeBooking(customer_bookings.get(y), days_bookings.get(y));
                    new BookingEntry().addBooking(AllFlights.get(z), customer_bookings.get(y), days_bookings.get(y));
                    if (y == customer_bookings.size()-1) {
                        break outerloop;
                    }
                }
                else {
                    i += 1;
                }
            }
            if (i == AllFlights.size()) {
                new BookingEntry().removeBooking(customer_bookings.get(y), days_bookings.get(y));
            }
        }
        
        int y = 0;
        outerloop:
        for (int a = 0; a < customer_waitlist.size(); a++) {
            y = 0;
            for (int b = 0; b < AllFlights.size(); b++) {
                if (BookingEntry.getFlightSeats(AllFlights.get(b).toString(), Date.valueOf(days_waitlist.get(a).toString())) < Flight.getSeats((String) AllFlights.get(b))) {
                    new BookingEntry().removeBooking(customer_waitlist.get(a), days_waitlist.get(a));
                    new BookingEntry().addBooking(AllFlights.get(b), customer_waitlist.get(a), days_waitlist.get(a));
                    if (a == customer_waitlist.size()-1) {
                        break outerloop;
                    }
                }
                else {
                    y += 1;
                }
            }
            if (y == AllFlights.size()) {
                new Waitlist().removeWaitlist(customer_waitlist.get(a), days_waitlist.get(a));
            }
        }
        
        jLabel22.setText("Flight Dropped");
    }//GEN-LAST:event_Drop_buttonActionPerformed

