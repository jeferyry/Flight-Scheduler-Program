    private void cancel_booking_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancel_booking_buttonActionPerformed
        // TODO add your handling code here:
        Object options = Delete_customer_combo_box.getSelectedItem();
        Object options1 = Delete_day_combo_box.getSelectedItem();
        Object customer_day_flight = null;
        Object new_customer_waitlist = null;
        
        customer_day_flight = BookingEntry.getCustomerDayFlight(options, options1); //get old customer day flight
        
        new BookingEntry().removeBooking(options, options1); //delete old customer's booking
        new Waitlist().removeWaitlist(options, options1); //delete old customer from waitlist if it's present
        
        new_customer_waitlist = Waitlist.getWaitListCustomer(customer_day_flight, options1); //get new customer from waitlist
        
        if (new_customer_waitlist != "") {
            new Waitlist().removeWaitlist(new_customer_waitlist, options1); // remove new customer from waitlist
            new BookingEntry().addBooking(customer_day_flight, new_customer_waitlist, options1); //book new customer
        }
        
        String text = "Customer " + String.valueOf(options) + "\n on Flight " + customer_day_flight + " was deleted";
        jLabel23.setText(text); //remember to change this 
        
        
    }//GEN-LAST:event_cancel_booking_buttonActionPerformed

