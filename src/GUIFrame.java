    private void AddBookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBookingActionPerformed
        // TODO add your handling code here:
        Object options = Flight_booking_combo.getSelectedItem();
        Object options1 = Customers_booking_combo.getSelectedItem();
        Object options2 = Dates_booking_combo.getSelectedItem();
        
        int count = 0;
        int seats = 0;
        
        count = BookingEntry.getFlightSeats(options.toString(), Date.valueOf(options2.toString()));
        seats = Flight.getSeats(Flight_booking_combo.getSelectedItem().toString());
        
        if (count < seats) {
            new BookingEntry().addBooking(options, options1, options2);
            Booking_label.setText("Booking Added");
        }
        
        else {
            new Waitlist().addWaitList(options, options1, options2);
            remove(Booking_label);
            Booking_label_2.setText("Booking added to Waitlist");
        }
    }//GEN-LAST:event_AddBookingActionPerformed

